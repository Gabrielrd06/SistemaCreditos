    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtApellidos.getText().equals("") || txtNombres.getText().equals("") || txtidentificacion.getText().equals("") || txtDireccion.getText().equals("")) {
            ClaseUtil.mensajeExito(this,4);
        } else {   
            
            controladorPersona.getSeleccionado().setDni(txtidentificacion.getText().trim().toUpperCase());                                                        
            controladorPersona.getSeleccionado().setApellidos(txtApellidos.getText().trim().toUpperCase()); 
            controladorPersona.getSeleccionado().setNombres(txtNombres.getText().trim().toUpperCase()); 
            controladorPersona.getSeleccionado().setTelefonoFijo(txtTelefono.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setCelular1(txtCelular.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setCelular2(txtCelular2.getText().toUpperCase());
            controladorPersona.getSeleccionado().setDireccion(txtDireccion.getText().trim().toUpperCase());       
            controladorPersona.getSeleccionado().setEmail(txtEmail.getText().trim().toUpperCase());             


            if (this.accion == Controlador.NUEVO) {
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorPersona.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }else if(this.accion == Controlador.MODIFICAR){
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorPersona.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }
            this.presentarDatos();
            this.accion = 0;          
            ClaseUtil.limpiarComponente(jPanelDatos);            
        }        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (!txtApellidos2.getText().equals("")) {
            listaPersonas.clear();
            listaPersonas.addAll(controladorPersona.buscarXPatron(txtApellidos2.getText().trim()));          
        }else if(!txtDni2.getText().equals("")){
            listaPersonas.clear();
            listaPersonas.addAll(controladorPersona.buscarXPatron(txtDni2.getText().trim()));             
        }else{
            this.presentarDatos();
        }               
    }//GEN-LAST:event_btnBuscarActionPerformed


