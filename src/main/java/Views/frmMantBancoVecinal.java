    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fila = jTablePersonaN.getSelectedRow();
        if (fila != -1) {
            this.accion = Controlador.MODIFICAR;
            this.activarComponents();
            controladorPersona.setSeleccionado(this.listaPersonas.get(fila));

            jComboBoxIdentificacion.setSelectedItem(controladorPersona.getSeleccionado().getTipoIdentificacion());
            txtNombreBanco.setText(controladorPersona.getSeleccionado().getNumIdentificacion());
            txtDireccion.setText(controladorPersona.getSeleccionado().getNombre());
            txtRepresentante.setText(controladorPersona.getSeleccionado().getTelefonoFijo());
            txtCelular.setText(controladorPersona.getSeleccionado().getCelular1());
            txtCelular2.setText(controladorPersona.getSeleccionado().getCelular2());
            txtDireccion.setText(controladorPersona.getSeleccionado().getDireccion());
            jComboBoxTipo.setSelectedIndex(getTipoCliente(controladorPersona.getSeleccionado().getTipoPersona().toString()));
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtDireccion.getText().equals("") || txtNombreBanco.getText().equals("") || jComboBoxIdentificacion.getSelectedIndex()==0 || jComboBoxTipo.getSelectedIndex()==0 || txtDireccion.getText().equals("")) {
            ClaseUtil.mensajeExito(this,4);
        } else {
            controladorPersona.getSeleccionado().setTipoIdentificacion(jComboBoxIdentificacion.getSelectedItem().toString());
            controladorPersona.getSeleccionado().setNumIdentificacion(txtNombreBanco.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setNombre(txtDireccion.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setTelefonoFijo(txtRepresentante.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setCelular1(txtCelular.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setCelular2(txtCelular2.getText().toUpperCase());
            controladorPersona.getSeleccionado().setDireccion(txtDireccion.getText().trim().toUpperCase());
            controladorPersona.getSeleccionado().setTipoPersona(setTipoCliente().charAt(0));

            if (this.accion == Controlador.NUEVO) {
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorPersona.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }else if(this.accion == Controlador.MODIFICAR){
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorPersona.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }
            this.presentarDatos();
            this.accion = 0;
            this.activarComponents();
            ClaseUtil.limpiarComponente(jPanelDatos);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

