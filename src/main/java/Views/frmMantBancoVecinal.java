    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        if (!txtBanco.getText().equals("")) {
            listaPersonas.clear();
            listaPersonas.addAll(controladorPersona.buscarXPatron(txtBanco.getText().trim()));
        }else if(!txtDniRepresentante.getText().equals("")){
            listaPersonas.clear();
            listaPersonas.addAll(controladorPersona.buscarXPatron(txtDniRepresentante.getText().trim()));
        }else{
            this.presentarDatos();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

